package LabelPropagationUtilities;

public class RandomColorChooser {

    public static int randomRedValue() {
        return (int) (Math.random() * 256);
    }

    public static int randomGreenValue() {
        return (int) (Math.random() * 256);
    }

    public static int randomBlueValue() {
        return (int) (Math.random() * 256);
    }
}


